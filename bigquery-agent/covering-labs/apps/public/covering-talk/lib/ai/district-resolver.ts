/**
 * 주소가 서비스 지역(서울/경기/인천) 외인지 판별
 * @returns true이면 서비스 불가 지역
 */
export function isOutOfServiceArea(address: string | null): boolean {
  if (!address) return false;
  const normalized = address.replace(/\s/g, "");
  // 서울/경기/인천 키워드가 있으면 무조건 서비스 지역
  if (normalized.includes("서울") || normalized.includes("경기") || normalized.includes("인천")) {
    return false;
  }
  // 서울 구 이름이 직접 포함되어 있으면 서비스 지역
  const guMatch = normalized.match(/([가-힣]+구)/);
  if (guMatch && VALID_REGIONS.includes(guMatch[1])) {
    return false;
  }
  return OUT_OF_SERVICE_PREFIXES.some((prefix) => normalized.includes(prefix));
}

/**
 * 인천 주소인지 판별 (인천시/인천광역시 + 구/군 패턴)
 */
function isIncheonAddress(address: string): boolean {
  const normalized = address.replace(/\s/g, "");
  // "인천" 키워드 포함 여부
  if (normalized.includes("인천")) return true;
  // 인천 구/군 이름이 있고, 서울/경기 컨텍스트가 아닌 경우 (미추홀구는 인천에만 존재)
  if (normalized.includes("미추홀구")) return true;
  if (normalized.includes("부평구")) return true;
  if (normalized.includes("계양구")) return true;
  if (normalized.includes("연수구")) return true;
  if (normalized.includes("남동구")) return true;
  if (normalized.includes("옹진군")) return true;
  if (normalized.includes("강화군")) return true;
  return false;
}

/**
 * 주소 문자열에서 지역 정보를 정적 테이블로 추출
 *
 * 매칭 우선순위:
 * 1. 인천 주소 판별 → "인천"
 * 2. 서울 "구" 직접 매칭 (강남구, 종로구 등)
 * 3. 경기도 "시+구" 패턴 → 시 매핑 (수원시 영통구 → "수원")
 * 4. 경기도 "구" 만으로 매핑 (영통구 → "수원")
 * 5. 경기도 시/군 직접 매칭 (성남시, 가평군 등)
 * 6. 서울 동→구 매핑
 * 7. 경기도 동/읍/면→시 매핑
 * 8. 인천 동→인천 매핑
 * 9. 경기도 도로명→시 매핑
 */
// 서비스 지역 외 광역시/도 — 조기 감지하여 null 반환 (잘못된 AI 추론 방지)
const OUT_OF_SERVICE_CITIES = [
  "대전", "대구", "부산", "광주", "울산", "세종",
  "충청", "충남", "충북", "전라", "전남", "전북", "경상", "경남", "경북",
  "강원", "제주", "전주", "청주", "천안", "아산",
];

function extractDistrictByRegex(address: string): string | null {
  const normalized = address.replace(/\s/g, "");

  // ── 0. 서비스 지역 외 주소 조기 감지 ──
  if (OUT_OF_SERVICE_CITIES.some((city) => normalized.includes(city))) {
    console.log(`[resolveDistrict] 서비스 지역 외 감지: ${address}`);
    return null;
  }

  // ── 1. 인천 주소 → "인천" ──
  if (isIncheonAddress(normalized)) return "인천";

  // ── 2. 서울 "구" 직접 매칭 ──
  const guMatch = normalized.match(/([가-힣]+구)/);
  if (guMatch) {
    const gu = guMatch[1];
    const resolvedGu = VALID_REGIONS.includes(gu)
      ? gu
      : VALID_REGIONS.find((r) => r.endsWith("구") && gu.endsWith(r) && gu !== r) ?? null;

    if (resolvedGu) {
      // 교차 검증: 주소의 "동"이 경기도에 속하면 AI가 구를 잘못 추측한 것
      // 예: "서울시 강북구 행신동" → 행신동은 고양시 → "고양" 반환
      const dongCheck = normalized.match(/([가-힣]+동)/g);
      if (dongCheck) {
        for (const dong of dongCheck) {
          const gyeonggiCity = GYEONGGI_DONG_TO_CITY[dong];
          if (gyeonggiCity) {
            // 동이 서울 해당 구에 실제로 속하는지 확인
            const seoulGu = SEOUL_DONG_TO_GU[dong];
            if (!seoulGu || seoulGu !== resolvedGu) {
              console.log(`[resolveDistrict] 구-동 불일치: ${resolvedGu} + ${dong} → 경기도 ${gyeonggiCity} 우선`);
              return gyeonggiCity;
            }
          }
        }
      }
      return resolvedGu;
    }

    // ── 3~4. 경기도 구 → 시 매핑 ──
    // "수원시 영통구" → "수원", 또는 단독 "영통구" → "수원"
    if (GYEONGGI_GU_TO_CITY[gu]) return GYEONGGI_GU_TO_CITY[gu];

    // 경기도도 접미사 매칭 (예: "수원시영통구" → "영통구" → 수원)
    const suffixGyeonggi = Object.keys(GYEONGGI_GU_TO_CITY).find((k) => gu.endsWith(k) && gu !== k);
    if (suffixGyeonggi) return GYEONGGI_GU_TO_CITY[suffixGyeonggi];
  }

  // ── 5. 경기도 시/군 직접 매칭 ──
  const cityMatch = normalized.match(/([가-힣]+)[시군]/);
  if (cityMatch) {
    const city = cityMatch[1];
    if (VALID_REGIONS.includes(city)) return city;
  }

  // ── 6. 서울 동 → 구 매핑 ──
  const dongMatches = normalized.match(/([가-힣]+동)/g);
  if (dongMatches) {
    for (const dong of dongMatches) {
      if (SEOUL_DONG_TO_GU[dong]) return SEOUL_DONG_TO_GU[dong];
      // "1동", "2동" 같은 행정동 번호 제거 후 재시도
      const baseDong = dong.replace(/\d+동$/, "동");
      if (baseDong !== dong && SEOUL_DONG_TO_GU[baseDong]) return SEOUL_DONG_TO_GU[baseDong];
    }
  }

  // ── 7. 경기도 동/읍/면 → 시 매핑 ──
  if (dongMatches) {
    // 주소에서 시 컨텍스트 추출 (예: "성남시" → "성남")
    const cityContext = normalized.match(/([가-힣]+)시/)?.[1] ?? null;
    for (const dong of dongMatches) {
      if (GYEONGGI_DONG_TO_CITY[dong]) {
        // 시 컨텍스트가 있으면 일치하는지 확인
        if (cityContext && GYEONGGI_DONG_TO_CITY[dong] !== cityContext) {
          // 컨텍스트 시와 불일치 → 시 접미사 키 시도 (예: 정자동_수원)
          const contextKey = `${dong.replace(/동$/, "동")}_${cityContext}`;
          if (GYEONGGI_DONG_TO_CITY[contextKey]) return GYEONGGI_DONG_TO_CITY[contextKey];
          // 컨텍스트 시를 직접 반환 (VALID_REGIONS에 있으면)
          if (VALID_REGIONS.includes(cityContext)) return cityContext;
        }
        return GYEONGGI_DONG_TO_CITY[dong];
      }
    }
  }
  // 읍/면 매칭
  const eupMyeonMatches = normalized.match(/([가-힣]+[읍면])/g);
  if (eupMyeonMatches) {
    for (const em of eupMyeonMatches) {
      if (GYEONGGI_DONG_TO_CITY[em]) return GYEONGGI_DONG_TO_CITY[em];
    }
  }

  // ── 8. 인천 동 → "인천" 매핑 ──
  if (dongMatches) {
    for (const dong of dongMatches) {
      if (INCHEON_DONG_TO_REGION[dong]) return INCHEON_DONG_TO_REGION[dong];
    }
  }

  // ── 9. 경기도 도로명 → 시 매핑 ──
  const roadMatch = normalized.match(/([가-힣]+로)/g);
  if (roadMatch) {
    for (const road of roadMatch) {
      if (GYEONGGI_ROAD_TO_CITY[road]) return GYEONGGI_ROAD_TO_CITY[road];
    }
  }

  return null;
}

/**
 * Claude Haiku + web_search로 주소 → 지역 추론
 */
async function resolveDistrictByAI(address: string): Promise<string | null> {
  try {
    const regionList = VALID_REGIONS.join(", ");

    const response = await withRetry(() => createMessage({
      model: "haiku",
      max_tokens: 100,
      system: `당신은 한국 행정구역 전문가입니다. 주어진 주소가 어느 지역에 속하는지 파악하세요.

우리 서비스 지역 목록: ${regionList}

규칙:
1. 서울 주소 → 해당 "구"를 출력 (예: "역삼동" → "강남구")
2. 인천 주소 → 무조건 "인천" 출력 (인천서구, 인천남동구 등 상관없이 모두 "인천")
3. 경기도 주소 → 해당 시/군 이름만 출력 (예: "분당구 정자동" → "성남", "시흥시 정왕동" → "시흥")
4. 반드시 위 서비스 지역 목록에 있는 지역명만 출력
5. 파악 불가능하면 "null" 출력
6. 지역명만 출력. 다른 텍스트 금지.`,
      messages: [{ role: "user", content: `주소: ${address}` }],
    }));

    const text = response.text.trim();

    if (text === "null" || !text) return null;

    // 서비스 지역 목록에 있는지 검증
    if (VALID_REGIONS.includes(text)) return text;

    // 부분 매칭 시도 (예: AI가 "인천서구"라고 답하면 "인천"으로 매칭)
    const partial = VALID_REGIONS.find(
      (r) => text.includes(r) || r.includes(text.replace(/[시군구]/g, ""))
    );
    return partial ?? null;
  } catch (err) {
    console.error("[resolveDistrict] AI 추론 실패:", err);
    return null;
  }
}

/**
 * 주소에서 서비스 지역(구/시군) 파악
 *
 * 1차: regex로 "구" 추출
 * 2차: extractCollectedInfo에서 추출한 district 확인
 * 3차: Claude Haiku로 "동" → "구" 추론
 */
export async function resolveDistrict(
  address: string | null,
  aiExtractedDistrict?: string | null
): Promise<string | null> {
  if (!address) {
    // address 없어도 aiExtractedDistrict로 매핑 시도
    if (aiExtractedDistrict) {
      // 서울 동→구 매핑 (개봉동 → 구로구)
      if (SEOUL_DONG_TO_GU[aiExtractedDistrict]) return SEOUL_DONG_TO_GU[aiExtractedDistrict];
      // 경기도 구→시 매핑 (영통구 → 수원)
      if (GYEONGGI_GU_TO_CITY[aiExtractedDistrict]) return GYEONGGI_GU_TO_CITY[aiExtractedDistrict];
      // 경기도 구→시 매핑 "구" 없이 (수지 → 수지구 → 용인)
      if (GYEONGGI_GU_TO_CITY[aiExtractedDistrict + "구"]) return GYEONGGI_GU_TO_CITY[aiExtractedDistrict + "구"];
      // 경기도 동→시 매핑 (판교동 → 성남)
      if (GYEONGGI_DONG_TO_CITY[aiExtractedDistrict]) return GYEONGGI_DONG_TO_CITY[aiExtractedDistrict];
      // 인천 동→인천 매핑
      if (INCHEON_DONG_TO_REGION[aiExtractedDistrict]) return INCHEON_DONG_TO_REGION[aiExtractedDistrict];
      // 인천 구/군 → "인천"
      if (INCHEON_GU_LIST.includes(aiExtractedDistrict) || aiExtractedDistrict.includes("인천")) return "인천";
      // fuzzy 매칭 (영등포 → 영등포구)
      const fuzzy = fuzzyMatchRegion(aiExtractedDistrict);
      if (fuzzy) return fuzzy;
    }
    return aiExtractedDistrict ?? null;
  }

  // 0차: 서비스 지역 외 주소 조기 감지 (울산, 대전, 부산 등 → null 즉시 반환)
  const addrNorm = address.replace(/\s/g, "");
  if (OUT_OF_SERVICE_CITIES.some((city) => addrNorm.includes(city))) {
    console.log(`[resolveDistrict] 서비스 지역 외 → null: ${address}`);
    return null;
  }

  // 1차: regex
  const regexResult = extractDistrictByRegex(address);
  if (regexResult) return regexResult;

  // 2차: AI 추출 결과가 유효한 지역이면 사용 (부분 매칭 포함)
  if (aiExtractedDistrict) {
    const matched = fuzzyMatchRegion(aiExtractedDistrict);
    if (matched) return matched;
  }

  // "구"가 없고 "동" 또는 도로명("로"/"길")이 있는 경우 AI 추론
  if (address.match(/[가-힣]+(동|로|길)/)) {
    console.log(`[resolveDistrict] 구 없이 동/도로명 감지 → AI 추론 시작: ${address}`);
    return resolveDistrictByAI(address);
  }

  return aiExtractedDistrict ?? null;
}
