// region_prices에 등록된 지역 목록
const VALID_REGIONS = regionPricesData.map((r) => r.region);

/**
 * 부분 매칭: "영등포" → "영등포구", "강남" → "강남구" 등
 */
function fuzzyMatchRegion(text: string): string | null {
  if (!text) return null;
  // 이미 정확히 있으면 그대로
  if (VALID_REGIONS.includes(text)) return text;
  // "구" 붙여서 시도 (영등포 → 영등포구)
  if (VALID_REGIONS.includes(text + "구")) return text + "구";
  // 경기도 구→시 매핑 (수지 → 수지구 → 용인, 영통 → 영통구 → 수원)
  if (GYEONGGI_GU_TO_CITY[text + "구"]) return GYEONGGI_GU_TO_CITY[text + "구"];
  if (GYEONGGI_GU_TO_CITY[text]) return GYEONGGI_GU_TO_CITY[text];
  // "구" 떼서 시도
  const stripped = text.replace(/구$/, "");
  if (stripped !== text) {
    const match = VALID_REGIONS.find((r) => r === stripped || r === stripped + "구");
    if (match) return match;
  }
  // 포함 매칭
  const partial = VALID_REGIONS.find(
    (r) => r.includes(text.replace(/[시군구]/g, "")) || text.includes(r.replace(/구$/, ""))
  );
  return partial ?? null;
}

